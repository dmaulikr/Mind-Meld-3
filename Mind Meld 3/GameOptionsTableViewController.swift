import UIKit

class GameOptionsTableViewController: UITableViewController {



    var gameOptions = [GameOption]()
    
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        self.gameOptions = [GameOption(name: "Start a New Game"), GameOption(name: "Resume Game"),GameOption(name: "High Scores")]
        self.tableView.reloadData()

    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
    }

    // MARK: - Table view data source

    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return self.gameOptions.count
    }
    

    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = self.tableView.dequeueReusableCellWithIdentifier("gameOptionCell", forIndexPath: indexPath) as UITableViewCell
        let gameOption = self.gameOptions[indexPath.row]
        cell.textLabel!.text = gameOption.name
        cell.accessoryType = UITableViewCellAccessoryType.DisclosureIndicator
        return cell
        
    }
    
     

